class RobertaOutput(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.intermediate_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states, input_tensor, reg_preds = False, hidden_cutoff = None, attention_mask = None, attn_weights = None):
        hidden_states = self.dense(hidden_states)
        updated_masks = attention_mask
        if hidden_cutoff == 0 or hidden_cutoff is None or reg_preds is False:
            hidden_states = self.dropout(hidden_states)
        elif hidden_cutoff > 0:#== 'attn_span_cutoff':
            #print('hi2')
            ori_mask = (1 - (attention_mask / -10000.0)).reshape([hidden_states.shape[0], hidden_states.shape[1]])
            
            input_lens = torch.sum(ori_mask, dim = 1)
            input_embeds, input_masks = [], []
            for i in range(hidden_states.shape[0]):
                if input_lens[i] * hidden_cutoff > 1:
                    cutoff_length = (input_lens[i] * hidden_cutoff).type(torch.long)
                else:
                    cutoff_length = 1
                    
                if input_lens[i] * 0.4 > 1:
                    k = (input_lens[i] * 0.4).type(torch.long)
                else:
                    k = 1
                
                top_imp_token_ind = torch.topk(attn_weights[i], k = input_lens[i].type(torch.long), dim = -1)[1]
                top_imp_token_ind = top_imp_token_ind[:k]
                
                shuffled_index = torch.randperm(len(top_imp_token_ind)).to(hidden_states.device)
                start = top_imp_token_ind[shuffled_index[0]]
                if start + cutoff_length > input_lens[i]:
                    cutoff_length = (input_lens[i] - start).type(torch.long)

                cutoff_embed = torch.cat((hidden_states[i][:start],
                                        torch.zeros([cutoff_length, hidden_states.shape[-1]],
                                                    dtype=torch.float).to(hidden_states.device),
                                        hidden_states[i][start + cutoff_length:]), dim=0)

                cutoff_mask = torch.cat((ori_mask[i][:start],
                                     torch.zeros([cutoff_length], dtype=ori_mask.dtype).to(hidden_states.device),
                                     ori_mask[i][start + cutoff_length:]), dim=0)

                input_embeds.append(cutoff_embed)
                input_masks.append(cutoff_mask)
            
            hidden_states = torch.stack(input_embeds, dim=0)
            
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        if hidden_cutoff is not None and hidden_cutoff > 0 and reg_preds:
            return hidden_states, updated_masks
        else:
            return hidden_states

# Copied from transformers.models.bert.modeling_bert.BertLayer with Bert->Roberta
class RobertaLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = RobertaAttention(config)
        self.is_decoder = config.is_decoder
        self.add_cross_attention = config.add_cross_attention
        if self.add_cross_attention:
            assert self.is_decoder, f"{self} should be used as a decoder model if cross attention is added"
            self.crossattention = RobertaAttention(config)
        self.intermediate = RobertaIntermediate(config)
        self.output = RobertaOutput(config)

    def forward(
        self,
        hidden_states,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        past_key_value=None,
        output_attentions=False,
        reg_preds=False,
        hidden_cutoff=None,
        ori_attention_mask=None,
    ):
        # decoder uni-directional self-attention cached key/values tuple is at positions 1,2
        self_attn_past_key_value = past_key_value[:2] if past_key_value is not None else None
        self_attention_outputs = self.attention(
            hidden_states,
            attention_mask,
            head_mask,
            output_attentions=output_attentions,
            past_key_value=self_attn_past_key_value,
            reg_preds=reg_preds,
            hidden_cutoff=hidden_cutoff,
        )
        attention_output = self_attention_outputs[0]

        ##### Added for hiddencut #####
        if hidden_cutoff is not None and reg_preds:
            #print('hi3')
            atten_weights = self_attention_outputs[1]
            avg_attn_weights = torch.sum(atten_weights, dim = -2)
            avg_attn_weights = torch.sum(avg_attn_weights, dim = 1) / 12.0

        # if decoder, the last output is tuple of self-attn cache
        if self.is_decoder:
            outputs = self_attention_outputs[1:-1]
            present_key_value = self_attention_outputs[-1]
        else:
            outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        cross_attn_present_key_value = None
        if self.is_decoder and encoder_hidden_states is not None:
            assert hasattr(
                self, "crossattention"
            ), f"If `encoder_hidden_states` are passed, {self} has to be instantiated with cross-attention layers by setting `config.add_cross_attention=True`"

            # cross_attn cached key/values tuple is at positions 3,4 of past_key_value tuple
            cross_attn_past_key_value = past_key_value[-2:] if past_key_value is not None else None
            cross_attention_outputs = self.crossattention(
                attention_output,
                attention_mask,
                head_mask,
                encoder_hidden_states,
                encoder_attention_mask,
                cross_attn_past_key_value,
                output_attentions,
            )
            attention_output = cross_attention_outputs[0]
            outputs = outputs + cross_attention_outputs[1:-1]  # add cross attentions if we output attention weights

            # add cross-attn cache to positions 3,4 of present_key_value tuple
            cross_attn_present_key_value = cross_attention_outputs[-1]
            present_key_value = present_key_value + cross_attn_present_key_value

        if hidden_cutoff is not None and reg_preds:
            intermediate_output = self.intermediate(attention_output)
            temp_attn = torch.clone(ori_attention_mask).detach()
            layer_output, updated_attention_mask = self.output(intermediate_output, attention_output, 
                reg_preds = reg_preds, hidden_cutoff = hidden_cutoff, attention_mask = temp_attn, attn_weights = avg_attn_weights
            )
        else:
            layer_output = apply_chunking_to_forward(
                self.feed_forward_chunk, self.chunk_size_feed_forward, self.seq_len_dim, attention_output
            )
            
        outputs = (layer_output,) + outputs

        # if decoder, return the attn key/values as the last output
        if self.is_decoder:
            outputs = outputs + (present_key_value,)

        if hidden_cutoff is not None and reg_preds:
            return outputs, updated_attention_mask
        else:
            return outputs

    def feed_forward_chunk(self, attention_output):
        intermediate_output = self.intermediate(attention_output)
        layer_output = self.output(intermediate_output, attention_output)
        return layer_output


# Copied from transformers.models.bert.modeling_bert.BertEncoder with Bert->Roberta
class RobertaEncoder(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.config = config
        self.layer = nn.ModuleList([RobertaLayer(config) for _ in range(config.num_hidden_layers)])

    def forward(
        self,
        hidden_states,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        past_key_values=None,
        use_cache=None,
        output_attentions=False,
        output_hidden_states=False,
        return_dict=True,
        reg_preds=False,
        hidden_cutoff=None,
    ):
        all_hidden_states = () if output_hidden_states else None
        all_self_attentions = () if output_attentions else None
        all_cross_attentions = () if output_attentions and self.config.add_cross_attention else None

        if self.training and reg_preds and hidden_cutoff is not None:
            ori_attention_mask = torch.clone(attention_mask)
            updated_attention_mask = attention_mask

        next_decoder_cache = () if use_cache else None
        for i, layer_module in enumerate(self.layer):
            ### Implementation of droplayer ###
            if self.config.droplayer_prob > 0 and self.training and reg_preds:
                #print('hi4')
                droplayer_mask = torch.empty(1).uniform_()
                if droplayer_mask <= self.config.droplayer_prob:
                    continue

            if output_hidden_states:
                all_hidden_states = all_hidden_states + (hidden_states,)

            layer_head_mask = head_mask[i] if head_mask is not None else None
            past_key_value = past_key_values[i] if past_key_values is not None else None

            if getattr(self.config, "gradient_checkpointing", False) and self.training:
                if use_cache:
                    logger.warning(
                        "`use_cache=True` is incompatible with `config.gradient_checkpointing=True`. Setting "
                        "`use_cache=False`..."
                    )
                    use_cache = False

                def create_custom_forward(module):
                    def custom_forward(*inputs):
                        return module(*inputs, past_key_value, output_attentions)

                    return custom_forward

                layer_outputs = torch.utils.checkpoint.checkpoint(
                    create_custom_forward(layer_module),
                    hidden_states,
                    attention_mask,
                    layer_head_mask,
                    encoder_hidden_states,
                    encoder_attention_mask,
                )
            else:
                if hidden_cutoff is not None and self.training and reg_preds:
                    #print('hi5')
                    layer_outputs, updated_attention_mask = layer_module(
                        hidden_states, 
                        updated_attention_mask, 
                        head_mask[i], 
                        encoder_hidden_states, 
                        encoder_attention_mask, 
                        reg_preds=reg_preds,
                        hidden_cutoff=hidden_cutoff, 
                        ori_attention_mask=ori_attention_mask,
                    )

                    if updated_attention_mask is None:
                        updated_attention_mask = ori_attention_mask
                else:
                    layer_outputs = layer_module(
                        hidden_states,
                        attention_mask,
                        layer_head_mask,
                        encoder_hidden_states,
                        encoder_attention_mask,
                        past_key_value,
                        output_attentions,
                        reg_preds=reg_preds,
                    )                    

            hidden_states = layer_outputs[0]
            if use_cache:
                next_decoder_cache += (layer_outputs[-1],)
            if output_attentions:
                all_self_attentions = all_self_attentions + (layer_outputs[1],)
                if self.config.add_cross_attention:
                    all_cross_attentions = all_cross_attentions + (layer_outputs[2],)

        if output_hidden_states:
            all_hidden_states = all_hidden_states + (hidden_states,)

        if not return_dict:
            return tuple(
                v
                for v in [
                    hidden_states,
                    next_decoder_cache,
                    all_hidden_states,
                    all_self_attentions,
                    all_cross_attentions,
                ]
                if v is not None
            )
        return BaseModelOutputWithPastAndCrossAttentions(
            last_hidden_state=hidden_states,
            past_key_values=next_decoder_cache,
            hidden_states=all_hidden_states,
            attentions=all_self_attentions,
            cross_attentions=all_cross_attentions,
        )


# Copied from transformers.models.bert.modeling_bert.BertPooler
class RobertaPooler(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.hidden_size)
        self.activation = nn.Tanh()

    def forward(self, hidden_states):
        # We "pool" the model by simply taking the hidden state corresponding
        # to the first token.
        first_token_tensor = hidden_states[:, 0]
        pooled_output = self.dense(first_token_tensor)
        pooled_output = self.activation(pooled_output)
        return pooled_output


class RobertaPreTrainedModel(PreTrainedModel):
    """
    An abstract class to handle weights initialization and a simple interface for downloading and loading pretrained
    models.
    """

    config_class = RobertaConfig
    base_model_prefix = "roberta"

    # Copied from transformers.models.bert.modeling_bert.BertPreTrainedModel._init_weights
    def _init_weights(self, module):
        """ Initialize the weights """
        if isinstance(module, nn.Linear):
            # Slightly different from the TF version which uses truncated_normal for initialization
            # cf https://github.com/pytorch/pytorch/pull/5617
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.bias is not None:
                module.bias.data.zero_()
        elif isinstance(module, nn.Embedding):
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.padding_idx is not None:
                module.weight.data[module.padding_idx].zero_()
        elif isinstance(module, nn.LayerNorm):
            module.bias.data.zero_()
            module.weight.data.fill_(1.0)


