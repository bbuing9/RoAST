        sequence_output = outputs[0]
        logits = self.classifier(sequence_output)
        logits_list.append(logits)

        # Inference of regularized way
        if self.config.r3f_noise_type is None and self.config.adv_eps == 0 and self.config.saliency_k == 0:
            temp_outputs, temp_logits, temp_loss = [], [], []
            loss_fct = CrossEntropyLoss()
            for i in range(self.config.n_infer):
                outputs = self.roberta(
                    input_ids,
                    attention_mask=attention_mask,
                    token_type_ids=token_type_ids,
                    position_ids=position_ids,
                    head_mask=head_mask,
                    inputs_embeds=inputs_embeds,
                    output_attentions=output_attentions,
                    output_hidden_states=output_hidden_states,
                    return_dict=return_dict,
                    reg_preds=reg_preds_flag,
                    hidden_cutoff=hidden_cutoff,
                )
                sequence_output = outputs[0]
                logits = self.classifier(sequence_output)
                
                p = torch.log_softmax(logits_list[0].view(-1, self.num_labels), dim=-1)
                p_tec = torch.softmax(logits_list[0].view(-1, self.num_labels), dim=-1)
                q = torch.log_softmax(logits.view(-1, self.num_labels), dim=-1)
                q_tec = torch.softmax(logits.view(-1, self.num_labels), dim=-1)

                kl_loss = torch.nn.functional.kl_div(p, q_tec, reduction='none')
                reverse_kl_loss = torch.nn.functional.kl_div(q, p_tec, reduction='none')
                loss = (kl_loss.sum() + reverse_kl_loss.sum()) / 2

                temp_outputs.append(outputs)
                temp_logits.append(logits)
                temp_loss.append(loss)

            if self.config.select == 'max':
                arg_loss = temp_loss.index(max(temp_loss))
            else:
                arg_loss = temp_loss.index(min(temp_loss))
            outputs_list.append(temp_outputs[arg_loss])
            logits_list.append(temp_logits[arg_loss])
        else:
            input_shape = input_ids.size()
            batch_size, seq_length = input_shape
            device = input_ids.device if input_ids is not None else inputs_embeds.device
            
            if attention_mask is None:
                attention_mask = torch.ones(((batch_size, seq_length + 0)), device=device)

            if token_type_ids is None:
                token_type_ids = torch.zeros(input_shape, dtype=torch.long, device=device)

            embedding_output = self.roberta.embeddings(input_ids=input_ids, position_ids=position_ids,
                token_type_ids=token_type_ids, inputs_embeds=inputs_embeds,
            )

            output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
            output_hidden_states = (
                output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
            )
            return_dict = return_dict if return_dict is not None else self.config.use_return_dict

            extended_attention_mask: torch.Tensor = self.get_extended_attention_mask(attention_mask, input_shape, device)

            if self.noise_sampler:
                # R3F training
                noise = self.noise_sampler.sample(sample_shape=embedding_output.shape).to(embedding_output)
                noised_embeddings = embedding_output.detach().clone() + noise

                encoder_outputs = self.roberta.encoder(noised_embeddings, attention_mask=extended_attention_mask, head_mask=head_mask,
                    encoder_hidden_states=None, encoder_attention_mask=None, past_key_values=None, use_cache=False,
                    output_attentions=output_attentions, output_hidden_states=output_hidden_states, return_dict=return_dict,
                )
                logits = self.classifier(encoder_outputs[0])
                logits_list.append(logits)
                outputs_list.append(logits) # dummy
            elif self.config.adv_eps > 0:
                noise = self.generate_noise(embedding_output)
                noised_embeddings = embedding_output.detach().clone() + noise

                bef_encoder_outputs = self.roberta.encoder(noised_embeddings, attention_mask=extended_attention_mask, head_mask=head_mask,
                    encoder_hidden_states=None, encoder_attention_mask=None, past_key_values=None, use_cache=False,
                    output_attentions=output_attentions, output_hidden_states=output_hidden_states, return_dict=return_dict,
                )
                bef_logits = self.classifier(bef_encoder_outputs[0])

                criterion = nn.CrossEntropyLoss()
                adv_loss = criterion(bef_logits, labels)
                delta_grad, = torch.autograd.grad(adv_loss, noise, only_inputs=True)
                norm = delta_grad.norm()
                if torch.isnan(norm) or torch.isinf(norm):
                    return 0
                delta_grad = self.norm_grad(delta_grad)

                adv_embeddings = noised_embeddings + delta_grad * self.config.adv_eps
                adv_embeddings = adv_embeddings.detach()

                encoder_outputs = self.roberta.encoder(adv_embeddings, attention_mask=extended_attention_mask, head_mask=head_mask,
                    encoder_hidden_states=None, encoder_attention_mask=None, past_key_values=None, use_cache=False,
                    output_attentions=output_attentions, output_hidden_states=output_hidden_states, return_dict=return_dict,
                )
                logits = self.classifier(encoder_outputs[0])
                logits_list.append(logits)
                outputs_list.append(logits) # dummy

        # Calculation training loss
        loss = None
        if labels is not None:
            if self.num_labels == 1:
                #  We are doing regression
                loss_fct = MSELoss()
                if loss is None:
                    loss = 0.5 * loss_fct(logits_list[0].view(-1), labels.view(-1))
                else:
                    loss += 0.5 * loss_fct(logits_list[-1].view(-1), labels.view(-1))
            else:
                loss_fct = CrossEntropyLoss()
                if loss is None:
                    loss = 0.5 * loss_fct(logits_list[0].view(-1, self.num_labels), labels.view(-1))
                else:
                    loss += 0.5 * loss_fct(logits_list[-1].view(-1, self.num_labels), labels.view(-1))

        if loss is not None:
            if self.num_labels == 1:
                loss_fct = MSELoss()
                loss += self.config.coeff_sym * loss_fct(logits_list[0].view(-1), logits_list[-1].view(-1))
            else:
                p = torch.log_softmax(logits_list[0].view(-1, self.num_labels), dim=-1)
                p_tec = torch.softmax(logits_list[0].view(-1, self.num_labels), dim=-1)
                q = torch.log_softmax(logits_list[-1].view(-1, self.num_labels), dim=-1)
                q_tec = torch.softmax(logits_list[-1].view(-1, self.num_labels), dim=-1)

                kl_loss = torch.nn.functional.kl_div(p, q_tec, reduction='none')
                reverse_kl_loss = torch.nn.functional.kl_div(q, p_tec, reduction='none')
                
                loss += self.config.coeff_sym * (kl_loss.sum() + reverse_kl_loss.sum()) / 2

        return loss, logits_list[0], outputs_list[0]


