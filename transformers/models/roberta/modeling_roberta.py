    def norm_grad(self, grad, epsilon=1e-8, norm_p='l_inf'):
        if norm_p == 'l2':
            direction = grad / (torch.norm(grad, dim=-1, keepdim=True) + epsilon)
        elif norm_p == 'l1':
            direction = grad.sign()
        else:
            direction = grad / (grad.abs().max(-1, keepdim=True)[0] + epsilon)
        return direction

    def roberta_kl(self, input_ids, attention_mask, token_type_ids, position_ids, head_mask, inputs_embeds,
                             labels, output_attentions, output_hidden_states, return_dict, hidden_cutoff):

        outputs_list = []
        logits_list = []

        # Regularized prediction flag
